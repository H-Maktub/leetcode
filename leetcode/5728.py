        print(temp)
        for i in range(1,n):
            if obstacles[i] !=1:
                temp[0][i] = temp[0][i-1]
            if obstacles[i] !=2:
                temp[1][i] = temp[1][i-1]
            if obstacles[i] !=3:
                temp[2][i] = temp[2][i-1]
            if obstacles[i] !=1:
                temp[0][i] = min(temp[0][i],min(temp[1][i],temp[2][i])+1)
